     public void getSum()
    {
        int sum = 0;
        for(int i = 0; i < jTable2.getRowCount(); i++)
        {
            sum = sum + Integer.parseInt(jTable2.getValueAt(i, 3).toString());
        } 
        txttotal.setText(Integer.toString(sum));
    }
     private void filter(String query) {
        TableRowSorter<DefaultTableModel> ft = new TableRowSorter<DefaultTableModel>(model);
        tabel_data.setRowSorter(ft);
        ft.setRowFilter(RowFilter.regexFilter(query));
    }
