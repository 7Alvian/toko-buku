    private void btntransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntransaksiActionPerformed
        try{
            String sql="select max(right(id_transaksi,3)) as id from penjualan";
            pst = (PreparedStatement) koneksi.prepareStatement(sql);
            Statement stmt = koneksi.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                if(rs.first()==false){
                 txttanggal.setText("T161");
                }else{
                    rs.last();
                    int autoid = rs.getInt(1) + 1;
                        String nomor = String.valueOf(autoid);
                        int noLong = nomor.length();
                        
                        for(int a = 0;a<3-noLong;a++)
                        {nomor = "0" + nomor;}
                        txtid.setText("T"+nomor);
                }       
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btntransaksiActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        int selectedRowIndex = tabel_data.getSelectedRow();
        
        Connection c = buka_koneksi();
        String sqlkode = "Update buku SET `stok`= stok - " + this.txtjumlah.getText() + " "
                + "Where id_buku=" + model.getValueAt(selectedRowIndex, 0) + "";
        int jumlah = Integer.parseInt(txtjumlah.getText());
        int harga = Integer.parseInt((String) model.getValueAt(selectedRowIndex, 3));
        int row3 = jumlah * harga;
        String sqlkode1 = "Insert into penjualan1 (`id_transaksi`,`id_barang`,`nama_barang`,`tanggal`,`jumlah`,`harga`) "
                + "values ('" + this.txtid.getText() + "',"
                + "'" + model.getValueAt(selectedRowIndex, 0) + "',"
                + "'" + model.getValueAt(selectedRowIndex, 1) + "',"
                + "'" + this.txttanggal.getText() + "',"
                + "'" + this.txtjumlah.getText() + "',"
                + "'" + row3 + "')";
        try {
            java.sql.PreparedStatement p1 = (java.sql.PreparedStatement) c.prepareStatement(sqlkode1);
            java.sql.PreparedStatement p2 = (java.sql.PreparedStatement) c.prepareStatement(sqlkode);
            p1.executeUpdate();
            p1.close();
            p2.executeUpdate();
            p2.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan" + ex.getMessage());
        }
        ambil_data_tabel();
        Object[] row = new Object[4];
        row[0] = model.getValueAt(selectedRowIndex, 0);
        row[1] = model.getValueAt(selectedRowIndex, 1);
        row[2] = txtjumlah.getText();
        row[3] = row3;
        model1.addRow(row);
        getSum();
    }//GEN-LAST:event_btntambahActionPerformed

